package common.util;

/**
 * An enumerated type for the returns of dialog pages.
 * <p>
 * okay = Okay button pressed
 * <p>
 * yes = Yes button pressed
 * <p>
 * no = No button pressed
 * <p>
 * cancel = Cancel button pressed
 * <p>
 * xOut = Page closed out
 */
public enum DialogReturns {
	okay,
	yes,
	no,
	cancel,
	xOut
}